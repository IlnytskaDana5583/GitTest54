//Класс "Калькулятор"
//создать класс Calculator, который будет представлен
//базовые арифметические операции: сложение, вычитание, умножение и деление.
//Класс должен сохранять:
//Статические методы для каждой математической операции, вводящие два аргумента и возвращающие результат операции

public class CalculatorStatic {
    public   static int
    sum( int a, int b) {
        return  a + b;
    }
    public static int
    diff( int a, int b) {
        return a - b;
    }
    public static int
    prod( int a, int b) {
        return a * b;
    }
    public static int
    quot( int a, int b) {
        return a / b;
    }

}



















